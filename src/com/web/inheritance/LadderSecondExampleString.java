package com.web.inheritance;

public class LadderSecondExampleString {

	public static void main(String[] args) {

		String str = "uc";

		if (str.equals("Chrome")) {

			System.out.println("Chrome browser started...");
		}

		else if (str.equals("FF")) {

			System.out.println("FF browser started...");
		}

		else if (str.equals("edge")) {

			System.out.println("edge browser started...");
		}

		else if (str.equals("ie")) {

			System.out.println("ie browser started...");
		} else {

			System.out.println("Hey Please choose the right browser name");
		}

	}

}
